
// ******** == e === *********
// == (igualdade):

// Este operador compara dois valores, permitindo a conversão de tipos durante a comparação. Se os valores forem diferentes em tipo, o JavaScript tentará convertê-los antes de fazer a comparação. Por exemplo, 1 == '1' é verdadeiro porque o JavaScript converte o segundo valor para o tipo número antes de compará-lo com o primeiro.
// === (igualdade estrita):

// Este operador também compara dois valores, mas não faz conversão de tipo durante a comparação. Os valores devem ser do mesmo tipo e ter o mesmo valor para a expressão ser verdadeira. Por exemplo, 1 === '1' é falso porque o primeiro é um número e o segundo é uma string.


// ******** != e !== *********
// != (diferente):

// Este operador verifica se dois valores são diferentes um do outro. Ele também faz conversão de tipos. Por exemplo, 1 != '1' é falso porque o JavaScript converte o segundo valor para o tipo número e, como ambos os valores são iguais numericamente, a expressão é falsa.
// !== (diferente estrito):

// Assim como o operador de igualdade estrita, este operador verifica se dois valores são diferentes, mas sem fazer conversão de tipo. Os valores devem ser de tipos diferentes ou ter valores diferentes para a expressão ser verdadeira.



// ******** >, >=, <, <= ********
// > (maior que):

// Este operador verifica se o valor da esquerda é estritamente maior do que o valor da direita.
// >= (maior ou igual a):

// Este operador verifica se o valor da esquerda é maior ou igual ao valor da direita.
// < (menor que):

// Este operador verifica se o valor da esquerda é estritamente menor do que o valor da direita.
// <= (menor ou igual a):

// Este operador verifica se o valor da esquerda é menor ou igual ao valor da direita.
